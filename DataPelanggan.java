class DataPelanggan {
    private String nama;
    private String barangPilihan;

    public DataPelanggan(String nama, String barangPilihan) {
        this.nama = nama;
        this.barangPilihan = barangPilihan;
    }

    public String getNama() {
        return nama;
    }

    public String getBarangPilihan() {
        return barangPilihan;
    }
}